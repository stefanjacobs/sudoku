package main

import "testing"

func TestIsValid(t *testing.T) {
	var valid = [9][9]int{
		{5, 8, 6, 0, 3, 1, 0, 7, 0},
		{2, 0, 7, 8, 6, 0, 5, 1, 3},
		{0, 1, 0, 7, 0, 5, 2, 0, 6},
		{0, 2, 8, 0, 0, 4, 3, 6, 1},
		{6, 0, 4, 9, 1, 3, 7, 2, 0},
		{0, 3, 1, 6, 2, 0, 0, 9, 5},
		{4, 0, 5, 0, 8, 2, 0, 3, 7},
		{1, 7, 0, 4, 9, 6, 8, 0, 2},
		{0, 6, 2, 3, 5, 0, 1, 0, 9}}
	if !isValid(valid) {
		t.Errorf("Valid configuration is identified as non valid")
	}

	var lineInvalid = [9][9]int{
		{5, 8, 6, 0, 3, 1, 0, 7, 0},
		{2, 0, 7, 8, 6, 0, 5, 1, 3},
		{0, 1, 0, 7, 0, 5, 2, 0, 6},
		{0, 2, 8, 0, 0, 4, 3, 6, 1},
		{6, 0, 4, 9, 1, 3, 7, 2, 0},
		{0, 3, 1, 6, 2, 0, 0, 9, 5},
		{4, 0, 5, 0, 8, 2, 0, 3, 7},
		{1, 7, 0, 4, 9, 6, 8, 0, 2},
		{0, 6, 2, 3, 5, 0, 1, 6, 9}} // <- 2x6
	if isValid(lineInvalid) {
		t.Errorf("Line error was not identified")
	}

	var columnInvalid = [9][9]int{
		{5, 8, 6, 0, 3, 1, 0, 7, 0},
		{2, 0, 7, 8, 6, 0, 5, 1, 3},
		{0, 1, 0, 7, 0, 5, 2, 0, 6},
		{0, 2, 8, 0, 0, 4, 3, 6, 1},
		{6, 0, 4, 9, 1, 3, 7, 2, 0},
		{0, 3, 1, 6, 2, 0, 0, 9, 5},
		{4, 0, 5, 0, 8, 2, 0, 0, 7},
		{1, 7, 0, 4, 9, 6, 8, 0, 3}, // <- 2x3
		{0, 6, 2, 3, 5, 0, 1, 0, 9}}
	if isValid(columnInvalid) {
		t.Errorf("Column error was not identified")
	}

	var sectorInvalid = [9][9]int{
		{5, 8, 6, 0, 3, 1, 0, 7, 0},
		{2, 0, 7, 8, 6, 0, 5, 1, 3},
		{0, 1, 0, 7, 0, 5, 2, 0, 6},
		{0, 2, 8, 0, 0, 9, 3, 6, 1}, // <- 2x9
		{6, 0, 4, 9, 1, 3, 7, 2, 0},
		{0, 3, 1, 6, 2, 0, 0, 9, 5},
		{4, 0, 5, 0, 8, 2, 0, 3, 7},
		{1, 7, 0, 4, 9, 6, 8, 0, 2},
		{0, 6, 2, 3, 5, 0, 1, 0, 9}}
	if isValid(sectorInvalid) {
		t.Errorf("Sector error was not identified")
	}

}

func TestIsFinal(t *testing.T) {
    var nonFinal = [9][9]int{
		{5, 8, 6, 0, 3, 1, 0, 7, 0},
		{2, 0, 7, 8, 6, 0, 5, 1, 3},
		{0, 1, 0, 7, 0, 5, 2, 0, 6},
		{0, 2, 8, 0, 0, 4, 3, 6, 1},
		{6, 0, 4, 9, 1, 3, 7, 2, 0},
		{0, 3, 1, 6, 2, 0, 0, 9, 5},
		{4, 0, 5, 0, 8, 2, 0, 3, 7},
		{1, 7, 0, 4, 9, 6, 8, 0, 2},
        {0, 6, 2, 3, 5, 0, 1, 0, 9}}
    if isFinal(nonFinal) {
        t.Errorf("Nonfinal Board was not identified")
    }
    var final = [9][9]int{
		{6, 3, 5, 7, 2, 1, 8, 4, 9},
		{2, 7, 8, 9, 4, 5, 1, 6, 3},
		{9, 1, 4, 6, 8, 3, 7, 2, 5},
		{4, 9, 7, 8, 5, 2, 6, 3, 1},
		{3, 8, 6, 1, 9, 4, 2, 5, 7},
		{1, 5, 2, 3, 7, 6, 9, 8, 4},
		{5, 4, 1, 2, 6, 7, 3, 9, 8},
		{7, 6, 9, 4, 3, 8, 5, 1, 2},
        {8, 2, 3, 5, 1, 9, 4, 7, 6}}
    if !isFinal(final) {
        t.Errorf("Final Board was not identified as final")
    }
}
